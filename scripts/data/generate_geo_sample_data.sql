-- Geo/Map/Choropleth Chart Sample Data | 地图图表示例数据
-- 用于测试地图功能的示例数据 (SQLite兼容)

CREATE TABLE IF NOT EXISTS geo_demo (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    region TEXT NOT NULL,        -- 地区名称（省份、国家、城市等）
    value REAL NOT NULL,         -- 数值
    longitude REAL,              -- 经度
    latitude REAL,               -- 纬度
    category TEXT,               -- 分类
    description TEXT,            -- 说明
    color TEXT,                  -- 颜色
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP
);

